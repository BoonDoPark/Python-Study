class Calculator:

    @staticmethod
    def add(first_dig, second_dig):
        return first_dig + second_dig

    @staticmethod
    def sub(first_dig, second_dig):
        return first_dig - second_dig

    @staticmethod
    def mul(first_dig, second_dig):
        return first_dig * second_dig

    @staticmethod
    def div(first_dig, second_dig):
        return first_dig / second_dig

